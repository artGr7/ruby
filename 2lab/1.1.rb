class Student
  def initialize(name, lastName, patronymic)
    @id
    @lastName = lastName
    @name = name
    @patronymic = patronymic
    @phone
    @telegram
    @email
    @git
  end
  
  
  def getid
		@id
  end
  
  
	def setid(id)
		@id = id
	end
	
  
	def getname
		@name
	end
	
  
  def setname(name)
		@name = name
	end
	
  
	def getlastName
		@lastName
	end
	
  
  def setlastName(lastName)
		@lastName = lastName
	end
	
	def getlastName
		@lastName
	end
	
  
  def setlastName(lastName)
		@lastName = lastName
	end

  
	def getphone
		@phone
	end
	
  
  def setphone(phone)
		@phone = phone
	end
	
	
  def gettelegram
		@telegram
	end
  
  
	def settelegram(telegram)
		@telegram = telegram
	end
	
	def getemail
		@email
	end
  
  
	def setemail(email)
		@email = email
	end
	
	def getgit
		@git
	end
  
  
	def setgit(git)
		@git = git
	end
  
  
end
